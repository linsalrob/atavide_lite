"""
Create FASTA files from the clusters generated by VAMB.

CANONICAL SCRIPT: This is the recommended script for creating VAMB bin FASTA files.

Unlike vamb_create_fasta.py, this script does NOT require the VAMB Python module,
making it version-independent and more robust across different VAMB installations.

This script:
1. Reads a VAMB clusters.tsv file (clustername<TAB>contigname format)
2. Reads the original assembly FASTA file
3. Writes each cluster to a separate compressed FASTA file

Key features:
- Works with any VAMB version (doesn't import vamb module)
- Supports minimum cluster size filtering (-m parameter)
- Memory efficient (streams FASTA file)
- Outputs compressed FASTA files (.fasta.gz)

Usage:
    python vamb_create_fasta_clusters.py -f contigs.fasta -c clusters.tsv -o bins_output/

Author: Rob Edwards
"""

import os
import sys
import gzip
import argparse
from atavide_lib import stream_fasta, colours

__author__ = 'Rob Edwards'


if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter
    )
    parser.add_argument(
        '-f', '--fasta',
        help='Input FASTA file (contigs/assembly)',
        required=True
    )
    parser.add_argument(
        '-o', '--output',
        help='Output directory for bin FASTA files',
        required=True
    )
    parser.add_argument(
        '-c', '--clusters',
        help='VAMB clusters.tsv file (clustername<TAB>contigname format)',
        required=True
    )
    parser.add_argument(
        '-m', '--minsize',
        type=int,
        default=0,
        help='Minimum total length of cluster to keep (bp). '
             'Note: this requires two passes of the FASTA file. Default: 0 (keep all)'
    )
    parser.add_argument(
        '-v', '--verbose',
        help='Verbose output',
        action='store_true'
    )
    args = parser.parse_args()

    # Validate inputs
    if not os.path.exists(args.fasta):
        print(f"Error: FASTA file not found: {args.fasta}", file=sys.stderr)
        sys.exit(1)
    
    if not os.path.exists(args.clusters):
        print(f"Error: Clusters file not found: {args.clusters}", file=sys.stderr)
        sys.exit(1)

    if args.verbose:
        print(f"{colours.GREEN}Reading the cluster file{colours.ENDC}", file=sys.stderr)

    # Read the cluster file
    clusters = {}
    with open(args.clusters) as f:
        for line in f:
            if line.startswith('#'):
                continue
            if line.startswith('clustername'):
                continue
            parts = line.strip().split('\t')
            if len(parts) < 2:
                continue
            cluster_id = parts[0]
            contig_id = parts[1]
            if contig_id in clusters:
                print(f"{colours.RED}Warning: {contig_id} is in multiple clusters{colours.ENDC}", file=sys.stderr)
            clusters[contig_id] = cluster_id

    if args.verbose:
        print(f"{colours.GREEN}Read {len(clusters)} contig assignments into {len(set(clusters.values()))} clusters{colours.ENDC}", file=sys.stderr)

    cluster_lengths = {x: 0 for x in set(clusters.values())}
    if args.minsize and args.minsize > 0:
        if args.verbose:
            print(f"{colours.GREEN}Filtering clusters to only keep those longer than {args.minsize} bp{colours.ENDC}", file=sys.stderr)
        # we need to read the fasta file and store the length of each contig
        for header, seq in stream_fasta(args.fasta):
            if header in clusters:
                cluster_lengths[clusters[header]] += len(seq)
        # now we only keep the clusters that are longer than the minimum length
        original_count = len(set(clusters.values()))
        clusters = {k: v for k, v in clusters.items() if cluster_lengths[v] >= args.minsize}
        filtered_count = len(set(clusters.values()))
        if args.verbose:
            print(f"{colours.GREEN}Kept {filtered_count}/{original_count} clusters after filtering{colours.ENDC}", file=sys.stderr)

    os.makedirs(args.output, exist_ok=True)
    filehandles = {}
    # now we stream the fasta file and write the contigs to the appropriate file, and save the filehandle in filehandles
    # then when we are done we close the filehandles
    if args.verbose:
        print(f"{colours.BLUE}Writing the fasta files to {args.output}{colours.ENDC}", file=sys.stderr)

    written_contigs = 0
    for header, seq in stream_fasta(args.fasta):
        if header not in clusters:
            continue
        cluster_id = clusters[header]
        if cluster_id not in filehandles:
            filename = os.path.join(args.output, f'{cluster_id}.fasta.gz')
            filehandles[cluster_id] = gzip.open(filename, 'wt')
        print(f">{header}\n{seq}", file=filehandles[cluster_id])
        written_contigs += 1

    for handle in filehandles.values():
        handle.close()
    
    if args.verbose:
        print(f"{colours.GREEN}Wrote {written_contigs} contigs to {len(filehandles)} bin files{colours.ENDC}", file=sys.stderr)
    else:
        print(f"Wrote {len(filehandles)} bins to {args.output}", file=sys.stderr)
